# a simple program that calculates sum, product and mean of the numbers

num1 = int(input("Give a number:" ))
num2 = int(input("Give a number:" ))
num3 = int(input("Give a number:" ))

print(f"sum: {num1 + num2 + num3}\n"
      f"product: {num1 * num2 * num3}\n"
      f"mean: {(num1 + num2 + num3) / 3}")